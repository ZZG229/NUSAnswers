package com.example.a229zzg.nusanswers;

public class Module {
    private String code;
    private String description;

    public Module() {

    }

    public Module(String code,String description){
        this.code = code;
        this.description = description;
    }

    public String getCode() {
        return code;
    }

    public String getDescription() {
        return description;
    }
}
