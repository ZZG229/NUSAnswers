package com.example.a229zzg.nusanswers;

public class UserInfo {
    String id;
    String Username;

    public UserInfo(){

    }

    public UserInfo(String id, String username) {
        this.id = id;
        Username = username;
    }

    public String getId() {
        return id;
    }

    public String getUsername() {
        return Username;
    }
}


